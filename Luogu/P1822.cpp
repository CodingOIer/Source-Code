int l, r;
int magic(int x)
{
    if (x < 10)
    {
        return x;
    }
    std::vector<int> v;
    int last = x % 10;
    x /= 10;
    for (; x != 0;)
    {
        v.push_back(abs(x % 10 - last));
        last = x % 10;
        x /= 10;
    }
    int res = 0;
    std::reverse(v.begin(), v.end());
    for (auto val : v)
    {
        res = res * 10 + val;
    }
    int r = magic(res);
    return r;
}
